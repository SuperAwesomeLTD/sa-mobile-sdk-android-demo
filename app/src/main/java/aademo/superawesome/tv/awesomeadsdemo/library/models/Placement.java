package aademo.superawesome.tv.awesomeadsdemo.library.models;

public class Placement {

    private int id;
    private String name;
    private String format;
    private int width;
    private int height;

    public Placement(){
        // do nothing
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getFormat() {
        return format;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
